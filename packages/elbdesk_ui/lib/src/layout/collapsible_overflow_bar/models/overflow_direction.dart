import 'package:elbdesk_ui/src/layout/collapsible_overflow_bar/widgets/collapsible_overflow_bar.dart';

/// Specifies the direction of overflow in the [CollapsibleOverflowBar].
///
/// The [OverflowDirection] enum is used to determine the direction in which
/// the children overflow, either left-to-right (ltr) or right-to-left (rtl).
enum OverflowDirection {
  /// Children overflow from left to right.
  ltr,

  /// Children overflow from right to left.
  rtl
}
