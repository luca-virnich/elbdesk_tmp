/// Specifies the position of the menu button in the `CollapsibleOverflowBar`.
///
/// The [MenuButtonPosition] enum is used to determine whether the menu button
/// should be placed at the start or end of the overflow bar.
enum MenuButtonPosition {
  /// The menu button is placed at the start of the overflow bar.
  start,

  /// The menu button is placed at the end of the overflow bar.
  end
}
