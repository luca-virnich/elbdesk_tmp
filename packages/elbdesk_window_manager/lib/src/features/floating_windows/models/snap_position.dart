/// Represents the position where a window can be snapped on the screen
enum SnapPosition {
  /// Left half of the screen
  left,
  
  /// Right half of the screen
  right,
  
  /// Left two-thirds of the screen
  leftTwoThirds,
  
  /// Right one-third of the screen
  rightOneThird,
  
  /// Left one-third of the screen
  leftOneThird,
  
  /// Right two-thirds of the screen
  rightTwoThirds,
  
  /// Top-left quarter of the screen
  topLeft,
  
  /// Top-right quarter of the screen
  topRight,
  
  /// Bottom-left quarter of the screen
  bottomLeft,
  
  /// Bottom-right quarter of the screen
  bottomRight,
}